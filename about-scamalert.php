<div class='container mb-5'><p>The Council comprises representatives from the commercial and industrial sectors, as well as from the public sector and the Singapore Police Force (SPF).<br>
The NCPC is incorporated as a charity and depends entirely on donations and sponsorships to run its programmes and activities. It is registered as a society with the Registry of Societies.</p>
<p>The NCPC is a catalyst, advisor and partner to mobilise the support of groups, organisations and individuals from the community to work closely with the Police to prevent crime. Specifically the objectives of the council are:</p>
<ul>
    <li>to raise the level of public awareness and concern about crime;</li>
    <li>encourage self-help in crime prevention;</li>
    <li>examine, develop and recommend crime prevention measures suitable to the public;</li>
    <li>coordinate efforts of organisations in crime prevention</li>
</ul>
<p>Since its inception in 1981, the Council had actively engaged the public through the organisation of crime prevention activities. Working closely with the Singapore Police Force, the Council holds the annual festive seasons crime prevention campaigns; as well as regular exhibitions and talks on crime prevention.</p>
<p>Various crime prevention projects initiated by the Council have been implemented within various quarters: such as the inclusion of the Hotel Security Award and Audit Scheme and the Construction Worksite Audit Scheme to boost security in hotels and worksites respectively. Others include the Delta League programme that aims to keep youths from committing crimes by getting them to do something that they like - play football.</p>
<p>The NCPC also provides financial support for the very popular Crime Watch TV programme, which has continued to enjoy high viewership throughout the years. The NCPC has forged close alliances with various professional, social and trade organisations to tackle the problem of crime, such as the Singapore Contractors Association Limited and the Singapore Hotel Association. In addition, various sub-committees have been formed under the NCPC to address problems of security in various areas; they are the Current Crime Committee, Community Engagement Committee, Commercial Sector Committee, Public Communications Committee and Fundraising Committee.</p>


